package basicprograms;

public class AssignmentOperators {

	public static void main(String[] args) {
		int a=45,b=10;
		// assignment operators
		a+=5;
		System.out.println(a);
		a-=10;
		System.out.println(a);
		b*=2;
		System.out.println(b);
		b/=5;
		System.out.println(b);
		b%=3;
		System.out.println(b);


	}

}
