package basicprograms;

public class LogicalOperators {

	public static void main(String[] args) {
		/*
		 * In logical operators the input and output is boolean form
		 */		int a=10,b=100,c=20;
		System.out.println((a>b)&&(a>c));
		System.out.println((a>b)||(a>c));
		System.out.println(!(a>b));
	}

}
