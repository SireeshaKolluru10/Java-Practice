package basicprograms;

public class RelationalOperators {

	public static void main(String[] args) {
		// relational operators -output is boolean value
		
		int a=10,b=20;
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a==b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a!=b);
	}

}
